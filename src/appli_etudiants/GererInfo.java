    private void jButtonGererInfoValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGererInfoValiderActionPerformed
        // TODO add your handling code here:

        //on récupère toutes les données du formulaire
        String modifNom = this.jTextFieldGererNom.getText();
        String modifPrenom = this.jTextFieldGererPrenom.getText();
        String modifMail = this.jTextFieldGererMail.getText();
        String modifRue = this.jTextFieldGererAdresseRue.getText();
        String modifVille = this.jTextFieldGererAdresseVille.getText();
        String modifCp = this.jTextFieldGererAdresseCp.getText();
        String modifTelPerso = this.jTextFieldGererTelPerso.getText();
        String modifTelPro = this.jTextFieldGererTelPro.getText();

        //on crée la requete avec les données
        String requeteSql = "update utilisateurs set "
                + "`nom`='" + modifNom
                + "', `prenom`='" + modifPrenom
                + "', `mail`='" + modifMail
                + "', `adresse_rue`='" + modifRue
                + "', `adresse_ville`='" + modifVille
                + "', `adresse_cp`='" + modifCp
                + "', `tel_personnel`='" + modifTelPerso
                + "',`tel_professionnel`='" + modifTelPro
                + "' where `id_utilisateur`=" + Connexion.getProfilConnecte().getId();

        // on utilise la classe BddConnect pour lancer la requete
        Integer resRequete = BddConnect.requeteAction(requeteSql);
        if (resRequete == 0) {
            this.jLabelEtatModif.setText("Echec de la modification");
            System.out.println(requeteSql);
            this.jLabelEtatModif.setForeground(Color.red);
        } else {
            Connexion.getProfilConnecte().modifPersonne(modifNom, modifPrenom, modifMail, modifRue, modifCp, modifVille, modifTelPerso, modifTelPro);
            this.jLabelEtatModif.setText("Modification réussie");
            this.jLabelEtatModif.setForeground(Color.green);
        }


    }//GEN-LAST:event_jButtonGererInfoValiderActionPerformed


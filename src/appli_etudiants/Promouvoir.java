        DefaultListModel modele = (DefaultListModel) this.jListPersonnes.getModel();
        String selectId = this.jListPersonnes.getSelectedValue();
        Integer selectIndex = this.jListPersonnes.getSelectedIndex();
        System.out.println("select id : " + selectId);
        System.out.println("select index : " + selectIndex);

        String requeteSql = "Update utilisateurs set id_role=2 where identifiant='" + selectId + "'";
        Integer res = BddConnect.requeteAction(requeteSql);

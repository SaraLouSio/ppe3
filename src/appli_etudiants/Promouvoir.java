    public Promouvoir(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();
        this.setLocationRelativeTo(parent);
        //on remplis la liste avec les utilisateurs
        String requeteSql = "select * from utilisateurs where id_role = 3";
        ResultSet utilisateurs = BddConnect.requeteSelection(requeteSql);

        DefaultListModel modele = new DefaultListModel();
        try {
            if (utilisateurs.next()) {
                utilisateurs.last();
                int longueur = utilisateurs.getRow();
                utilisateurs.first();
                for (int i = 1; i <= longueur; i++) {
                    String identifiant = utilisateurs.getString("identifiant");
                    //((DefaultListModel)jListPersonnes.getModel()).addElement(identifiant);
                    modele.addElement(identifiant);
                    this.jListPersonnes.setModel(modele);
                    System.out.println(identifiant);
                    if (i < longueur) {
                        utilisateurs.next();
                    }
                }

            }
        } catch (SQLException e) {

        }

    }

